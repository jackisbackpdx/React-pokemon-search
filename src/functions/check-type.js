export default function(type) {
            let color = '';
            if (type === 'bug') {
                color = '#8A980F'
            }
            if (type === 'grass') {
                color = '#389305'
            }
            if (type === 'ice') {
                color = '#6DD3F5'
            }
            if (type === 'dark') {
                color = '#453227'
            }
            if (type === 'dragon') {
                color = '#725adb'
            }
            if (type === 'electric') {
                color = '#eeaa0f'
            }
            if (type === 'fairy') {
                color = '#E39CE3'
            }
            if (type === 'fighting') {
                color = '#7F331C'
            }
            if (type === 'fire') {
                color = '#DD3307'
            }
            if (type === 'flying') {
                color = '#5D73D4'
            }
            if (type === 'ghost') {
                color = '#6161B3'
            }
            if (type === 'ground') {
                color = '#D1B055'
            }
            if (type === 'normal') {
                color = '#C5BFB4'
            }
            if (type === 'poison') {
                color = '#914492'
            }
            if (type === 'psychic') {
                color = '#EC447E'
            }
            if (type === 'rock') {
                color = '#9E873D'
            }
            if (type === 'steel') {
                color = '#B0B0BE'
            }
            if (type === 'water') {
                color = '#3899FC'
            }
            return color;
}